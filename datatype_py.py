#1. str
x = "Hello World"
print(type(x))

#2. int
x = 1
print(type(x))

#3. float
x = 1.9
print(type(x))

#4. complex
x = 2j
print(type(x))

#5. list
x = ["apple", "banana", "cherry"]
print(type(x))

#6. tuple
x = ("apple", "banana", "cherry")
print(type(x))

#7. range
x = range(6)
print(type(x))

#8. dict
x = {"name" : "John", "age" : 36}
print(type(x))

#9. set
x = {"apple", "banana", "cherry"}
print(type(x))

#10. frozenset
x = frozenset({"apple", "banana", "cherry"})
print(type(x))

#11. bool
x = True
print(type(x))

#12. bytes
x = b"Hello"
print(type(x))

#13. bytearray
x=bytearray(5)
print(type(x))

#14. memoryview
x = memoryview(bytes(5))
print(type(x))

#15. NoneType
x = None
print(type(x))